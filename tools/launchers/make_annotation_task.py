"""
make_annotation_task.py - prepare portable Mask Picker task folders/zips.

The package keeps the normal workspace layout:
  images/
  output/<model>/{overlay,npy,png,yolo}/
  selected/
  polygons/
  skipped/
  labels.json
  selections.json
  README_FOR_ANNOTATOR.md

This is intentionally different from the final dataset pack in bake_all.py:
annotation tasks include model predictions so another participant can keep
working without rerunning segmentation.
"""
from __future__ import annotations

import argparse
import json
import math
import shutil
import sys
import zipfile
from datetime import datetime, timezone
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
DEFAULT_DATA_DIR = ROOT / "data" / "vesicles_good"
DEFAULT_OUTPUT_DIR = ROOT / "_send" / "annotation_tasks"
DEFAULT_README = ROOT / "docs" / "README_FOR_ANNOTATOR_VESICLES.md"

IMAGE_EXTS = {".jpg", ".jpeg", ".png", ".tif", ".tiff"}
MODEL_FORMAT_EXTS = {
    "overlay": ".png",
    "png": ".png",
    "npy": ".npy",
    "yolo": ".txt",
}

